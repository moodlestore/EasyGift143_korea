			instagram: 'Instagram   ',  	// 9 + 3 = 12Í∏ÄÏûê
			x: 'X           ',          	// 1 + 11 = 12Í∏ÄÏûê  
			threads: 'Threads     '     	// 7 + 5 = 12Í∏ÄÏûê
